package bean;

public class Collections {

	private int user_id;
	
	private int goods_id;
	
	private String collections_create_time;

	public int getUser_id() {
		return user_id;
	}

	public void setUser_id(int user_id) {
		this.user_id = user_id;
	}

	public int getGoods_id() {
		return goods_id;
	}

	public void setGoods_id(int goods_id) {
		this.goods_id = goods_id;
	}

	public String getCollections_create_time() {
		return collections_create_time;
	}

	public void setCollections_create_time(String collections_create_time) {
		this.collections_create_time = collections_create_time;
	}
}
